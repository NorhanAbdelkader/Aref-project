export const validatePasswoed = (password) => {
    // Stackoverflow
    // minimum eight characters, at least one letter, one number and one special character:
    return String(password)
      .toLowerCase()
      .match(
        /^(?=.*[A-Za-z])(?=.*\d)(?=.*[@$!%*#?&])[A-Za-z\d@$!%*#?&]{8,}$/
        );
  };
    